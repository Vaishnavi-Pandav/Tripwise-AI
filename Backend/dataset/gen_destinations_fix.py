BUDGETS = {"beach":(8000,25000),"hill_station":(5000,20000),"city":(10000,40000),
           "heritage":(6000,22000),"religious":(3000,15000),"nature":(5000,18000),
           "adventure":(8000,30000),"village":(2000,8000),"national_park":(5000,20000),
           "desert":(6000,22000),"island":(15000,60000),"backwater":(8000,25000),"ski_resort":(12000,40000)}

cycle_iter = itertools.cycle(ALL_DESTINATIONS)
rows = []
for i in range(1000):
    name, state, country, cat, (lat, lon) = next(cycle_iter)
    suffix = f" ({i // len(ALL_DESTINATIONS) + 1})" if i >= len(ALL_DESTINATIONS) else ""
    bmin, bmax = BUDGETS.get(cat, (5000, 20000))
    rows.append({
        "id": str(uuid.uuid4()),
        "destination_name": name + suffix,
        "city": name,
        "state_region": state,
        "country": country,
        "category": cat,
        "description": DESCRIPTIONS.get(cat, "Beautiful destination."),
        "best_time_to_visit": random.choice(["Oct-Mar","Nov-Feb","Apr-Jun","Jul-Sep","Year-round"]),
        "average_budget": random.randint(bmin, bmax),
        "safety_score": round(random.uniform(5.5, 9.5), 1),
        "popularity_score": round(random.uniform(5.0, 9.8), 1),
        "latitude": round(lat + random.uniform(-0.05, 0.05), 4),
        "longitude": round(lon + random.uniform(-0.05, 0.05), 4),
        "image_url": IMAGES.get(cat, "https://images.unsplash.com/photo-1488085061387-422e29b40080?w=800"),
    })

df = pd.DataFrame(rows)
